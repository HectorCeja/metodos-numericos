    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!jMenuItem10.isEnabled()) {
            metodo.biseccion(jTextField1, jTextField2, jTextField3, jTextField4, jTextField5, tablaBiseccion, jTextField6, panelFuncion);
        } else if (!jMenuItem9.isEnabled()) {
            metodo.falsaPosicion(jTextField1, jTextField2, jTextField3, jTextField4, jTextField5, tablaBiseccion, jTextField6, panelFuncion);
        } else if (!jMenuItem8.isEnabled()) {
            metodo.secante(jTextField1, jTextField2, jTextField3, jTextField4, jTextField5, tablaBiseccion, jTextField6, panelFuncion);
        } else if (!jMenuItem7.isEnabled()) {
            metodo.newton1(jTextField1, jTextField2, jTextField4, jTextField5, tablaBiseccion, jTextField6, panelFuncion);
        } else if (!jMenuItem5.isEnabled()) {
            metodo.aproximaciones(jTextField1, jTextField2, jTextField4, jTextField5, tablaBiseccion, jTextField6, panelFuncion);
        } else if (!jMenuItem6.isEnabled()) {
            metodo.newton2(jTextField1, jTextField2, jTextField4, jTextField5, tablaBiseccion, jTextField6, panelFuncion);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

