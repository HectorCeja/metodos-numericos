    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");

            panelFuncion.removeAll();
            panelFuncion.repaint();
            for (int i = 0; i < tablaBiseccion.getRowCount(); i++) {

                tablaBiseccion.setValueAt(null, i, 0);
                for (int j = 1; j < tablaBiseccion.getColumnCount(); j++) {

                    tablaBiseccion.setValueAt(null, i, j);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Los elementos están vacíos");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

